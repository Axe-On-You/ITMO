package ru.itmo.prog.lab5.utility.console;

/**
 * Интерфейс консоли для взаимодействия с пользователем.
 * Определяет методы для вывода данных и управления приглашениями.
 * @author pmih
 */
public interface Console {
    /**
     * Выводит объект без перевода строки.
     * @param obj Объект для вывода.
     */
    void print(Object obj);

    /**
     * Выводит объект с переводом строки.
     * @param obj Объект для вывода.
     */
    void println(Object obj);

    /**
     * Выводит ошибку в стандартный поток ошибок.
     * @param obj Объект-ошибка.
     */
    void printError(Object obj);

    /**
     * Выводит два объекта в табличном формате (например: "Ключ : Значение").
     * @param obj1 Первый объект (ключ).
     * @param obj2 Второй объект (значение).
     */
    void printTable(Object obj1, Object obj2);

    /**
     * Устанавливает стандартное приглашение (PS1).
     */
    void ps1();

    /**
     * Устанавливает вторичное приглашение (PS2) для многострочного ввода.
     */
    void ps2();

    /**
     * @return Текущее приглашение PS1.
     */
    String getPS1();

    /**
     * @return Текущее приглашение PS2.
     */
    String getPS2();
}